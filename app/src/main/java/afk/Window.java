    private void enableBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_enableBtnMouseClicked
        app.start(timeSlider.getValue());
        statusLabel.setText("enabled");
        statusLabel.setForeground(Color.decode("#18864b"));
        timeSlider.setEnabled(false);
    }//GEN-LAST:event_enableBtnMouseClicked

    private void disableBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_disableBtnMouseClicked
        app.stop();
        statusLabel.setText("disabled");
        statusLabel.setForeground(Color.red);
        timeSlider.setEnabled(true);
    }//GEN-LAST:event_disableBtnMouseClicked

